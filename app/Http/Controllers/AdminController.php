    public function reply(Request $request,$id){
     $sup = Support::findOrfail($id);
     $sup->reply=$request->reply;
     $sup->replied=1;
     $sup->hide=0;
     $sup->update();
     return redirect()->back();
   }

   public function removeSup($id){
      $sup=Support::findOrfail($id);
      $sup->delete();
